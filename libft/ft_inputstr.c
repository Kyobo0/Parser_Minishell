#include "libft.h"

int	ft_inputstr(char *s)
{
	int	i;

	if (s == NULL)
		s = "(null)";
	i = 0;
	if (s)
	{
		while (s[i])
		{
			write(1, &s[i], 1);
			i++;
		}
	}
	return (i);
}
